package practice_pkg;

public class DataTypes 
{
	public static void main(String[] args)
	{
		boolean b=true;
		System.out.println("Value of boolean " +b);

		char a ='a';
		System.out.println("value of char "+a);

		char c= 65;
		System.out.println("Value of char at c "+c);

		byte by= 125;
		System.out.println("Value of byte "+by);

		short sh=135;
		System.out.println("value of short "+sh);

		int in=99602960;

		System.out.println("value of int "+in);

		long ln= 996029607;

		System.out.println("Value of long "+ln );

		float fl= 12.3966666666666f;

		System.out.println("float value is "+fl);
		
		double dl=12.3966666666666d;
		
		System.out.println("Value of double is "+dl);
		
		int flt= (int) 12.3966666666666;
		
		System.out.println("float downcast to int " +flt);

	}
}
